            String tno=txtno.getText();
            String tname=txtname.getText();
            String tstart=txtplace.getText();
            String tdest=txtdestination.getText();
            String tprice=txtprice.getText();
            pst=con.prepareStatement ("insert into busdetails(busno,busname,startplace,destination,price)Values(?,?,?,?,?)");

            pst.setString(1,tno);
            pst.setString(2,tname);
            pst.setString(3,tstart);
            pst.setString(4,tdest);
