    public void Bus_Details()
    {
        try {
            pst = con.prepareStatement("select * from busdetails");
            rs = pst.executeQuery();
            ResultSetMetaData rsd = rs.getMetaData();
            int c;
            c=rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            while(rs.next())
            {
                Vector v = new Vector();
                for( int i=0;i<=c;i++)
                {
                    v.add(rs.getString("busno"));
                     v.add(rs.getString("busname"));
                      v.add(rs.getString("startplace"));
                       v.add(rs.getString("destination"));
                        v.add(rs.getString("price"));
                }
                d.addRow(v);
            }
        } catch (SQLException ex) {
            Logger.getLogger(addbus.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
    
    
    
    
    
    
    

